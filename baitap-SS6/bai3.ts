let num1: number = 10;
let num2: number = 5;
let result: number;

// Phép cộng
result = num1 + num2;
console.log("Tổng:", result);

// Phép trừ
result = num1 - num2;
console.log("Hiệu:", result);

// Phép nhân
result = num1 * num2;
console.log("Tích:", result);

// Phép chia
result = num1 / num2;
console.log("Thương:", result);

// Phép chia lấy phần dư
result = num1 % num2;
console.log("Phần dư khi chia:", result);